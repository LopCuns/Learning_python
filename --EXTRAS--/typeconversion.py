# El type conversión es el proceso por el cual transformamos un objeto de un tipo a otro
# Puede ser implícito (si Python lo hace automáticamente) o explícito (también conocido como type casting,si lo hacemos nosotros los programadores a mano)

# Ejemplos

# Implicit type conversion

floatnum = 2.5
intnum = 2
sumof = floatnum + intnum
print(sumof,type(sumof))